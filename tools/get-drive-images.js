#!/usr/bin/env node
/**
 * Google Drive Image Link Fetcher
 * ================================
 * One-time utility: lists all image files in a Google Drive folder and
 * writes a ready-to-paste JS array of public URLs to stdout + a .js file.
 *
 * SETUP (do this once)
 * --------------------
 * 1. Go to https://console.cloud.google.com/
 * 2. Create a project (or pick existing one)
 * 3. Enable "Google Drive API"
 * 4. Go to "Credentials" → "Create Credentials" → "API Key"
 * 5. Copy the API key
 *
 * 6. In Google Drive, open your photos folder
 * 7. Right-click folder → "Share" → set to "Anyone with the link can view"
 * 8. Copy the folder URL — the FOLDER_ID is the long string at the end:
 *      https://drive.google.com/drive/folders/FOLDER_ID
 *
 * 9. Create a file called .env in this tools/ folder:
 *      GDRIVE_API_KEY=your_api_key_here
 *      GDRIVE_FOLDER_ID=your_folder_id_here
 *
 * RUN
 * ---
 *   cd tools
 *   npm install
 *   node get-drive-images.js
 *
 *   # Or pass values directly:
 *   GDRIVE_API_KEY=xxx GDRIVE_FOLDER_ID=yyy node get-drive-images.js
 *
 * OUTPUT
 * ------
 *   - Prints the JS array to console
 *   - Writes output/tilak-images.js  (ready to copy into tilak.js)
 */

const { google } = require('googleapis');
const fs   = require('fs');
const path = require('path');

// ── Load .env if present ──────────────────────────────────────────────
const envFile = path.join(__dirname, '.env');
if (fs.existsSync(envFile)) {
  fs.readFileSync(envFile, 'utf8')
    .split('\n')
    .forEach(line => {
      const [key, ...rest] = line.split('=');
      if (key && rest.length) process.env[key.trim()] = rest.join('=').trim();
    });
}

const API_KEY   = process.env.GDRIVE_API_KEY;
const FOLDER_ID = process.env.GDRIVE_FOLDER_ID;

if (!API_KEY || !FOLDER_ID) {
  console.error(`
ERROR: Missing required environment variables.

Create a file  tools/.env  with:
  GDRIVE_API_KEY=your_api_key_here
  GDRIVE_FOLDER_ID=your_folder_id_here

See the setup instructions at the top of this file.
`);
  process.exit(1);
}

// ── Supported image MIME types ────────────────────────────────────────
const IMAGE_MIMES = [
  'image/jpeg',
  'image/png',
  'image/webp',
  'image/gif',
  'image/heic',
  'image/heif',
];

const MIME_FILTER = IMAGE_MIMES.map(m => `mimeType='${m}'`).join(' or ');

/**
 * Convert a Google Drive file ID to the direct-display lh3 URL.
 * Requires the file to be shared "Anyone with the link".
 */
function toPublicUrl(fileId) {
  return `https://lh3.googleusercontent.com/d/${fileId}`;
}

/**
 * Fetch all image files from the given Drive folder (handles pagination).
 * Returns array of { id, name, webViewLink } objects.
 */
async function listAllImages(drive, folderId) {
  const files = [];
  let pageToken = null;

  do {
    const res = await drive.files.list({
      q: `'${folderId}' in parents and (${MIME_FILTER}) and trashed = false`,
      fields: 'nextPageToken, files(id, name, mimeType)',
      orderBy: 'name',           // alphabetical — matches typical camera filenames
      pageSize: 1000,
      pageToken: pageToken || undefined,
    });

    files.push(...res.data.files);
    pageToken = res.data.nextPageToken;
    process.stderr.write(`  Fetched ${files.length} files so far...\r`);
  } while (pageToken);

  process.stderr.write('\n');
  return files;
}

async function main() {
  console.error('Connecting to Google Drive API...');

  const drive = google.drive({ version: 'v3', auth: API_KEY });

  // Verify the folder exists and is accessible
  try {
    const folder = await drive.files.get({
      fileId: FOLDER_ID,
      fields: 'id, name',
    });
    console.error(`Folder found: "${folder.data.name}"`);
  } catch (err) {
    console.error(`\nERROR: Cannot access folder "${FOLDER_ID}"`);
    console.error('Make sure the folder is shared as "Anyone with the link can view".');
    console.error('Details:', err.message);
    process.exit(1);
  }

  console.error('Fetching image list...');
  const files = await listAllImages(drive, FOLDER_ID);

  if (files.length === 0) {
    console.error('No images found in that folder.');
    process.exit(0);
  }

  console.error(`Found ${files.length} images.`);

  // ── Build output ──────────────────────────────────────────────────
  const urlLines = files.map(f => `  '${toPublicUrl(f.id)}',  // ${f.name}`);

  const jsOutput = `// Auto-generated by tools/get-drive-images.js
// Folder ID : ${FOLDER_ID}
// Generated : ${new Date().toISOString()}
// Total     : ${files.length} images
//
// Paste this array into js/tilak.js  (replace the existing 'images' array)

const images = [
${urlLines.join('\n')}
];
`;

  // Write to output file
  const outDir = path.join(__dirname, 'output');
  if (!fs.existsSync(outDir)) fs.mkdirSync(outDir);

  const outFile = path.join(outDir, 'tilak-images.js');
  fs.writeFileSync(outFile, jsOutput, 'utf8');

  console.error(`\nDone! Output written to: tools/output/tilak-images.js`);
  console.error('Copy the "images" array from that file into js/tilak.js\n');

  // Also print to stdout so it can be piped
  console.log(jsOutput);
}

main().catch(err => {
  console.error('Unexpected error:', err.message);
  process.exit(1);
});
